class Solution{   
public:
    int klengthpref(string arr[], int n, int k, string str){    
         if(k>str.size()) 
         return 0;//base condition is mentioned here
        int c=0;
        for(int  i=0;i<n;i++)
        {
            if(arr[i].substr(0,k)==str.substr(0,k)) //logical part is here//it will take string as prefix from 0 to the length of k 
            c++;
        }
        return c;
    }
};

