class Solution {
  public:
    int setBits(int N) {
        if(N==0)return 0;
        int cnt=1;
        while(N!=1){
            if(N%2==1)cnt++;
            N/=2;
        }
        return cnt;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int N;
        cin >> N;

        Solution ob;
        int cnt = ob.setBits(N);
        cout << cnt << endl;
    }
    return 0;
}

